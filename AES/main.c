#include "stdio.h"
#include "../xxHash/xxhash.h"
#define HASH_SPACE 1024
#define SEED 5328

// clang main.c ../xxHash/libxxhash.0.8.2.dylib -o dv_hash_test

//the AES benchmark
int store(int* hash,char* buf,XXH32_hash_t seed){
    int location=(uint64_t)XXH32(buf,64,seed) % HASH_SPACE;
    unsigned counter=1;
    __asm("NOP\n");
    while(hash[location]!=0){
      location=(location+counter*counter)%HASH_SPACE;
      counter++;
      if(counter>128){
        return -1;
      }
    }
    return location;
}

int main(void)
{
  /*
  setting up the initial vector and the encryption keys
  */
  struct AES_ctx ctx;
  uint8_t key[] = { 0x2b, 0x7e, 0x15, 0x16, 0x28, 0xae, 0xd2, 0xa6, 0xab, 0xf7, 0x15, 0x88, 0x09, 0xcf, 0x4f, 0x3c };
  uint8_t in[]  = { 0x76, 0x49, 0xab, 0xac, 0x81, 0x19, 0xb2, 0x46, 0xce, 0xe9, 0x8e, 0x9b, 0x12, 0xe9, 0x19, 0x7d,
                    0x50, 0x86, 0xcb, 0x9b, 0x50, 0x72, 0x19, 0xee, 0x95, 0xdb, 0x11, 0x3a, 0x91, 0x76, 0x78, 0xb2,
                    0x73, 0xbe, 0xd6, 0xb8, 0xe3, 0xc1, 0x74, 0x3b, 0x71, 0x16, 0xe6, 0x9e, 0x22, 0x22, 0x95, 0x16,
                    0x3f, 0xf1, 0xca, 0xa1, 0x68, 0x1f, 0xac, 0x09, 0x12, 0x0e, 0xca, 0x30, 0x75, 0x86, 0xe1, 0xa7 };

  uint8_t iv[]  = { 0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f };

  AES_init_ctx_iv(&ctx, key, iv);

  int* hash=(int *)malloc(HASH_SPACE*sizeof(int));

  //encryption test
  for(int i=0;i<5;i++){
    for(int j=0;j<(2<<i);j++){
      AES_CBC_encrypt_buffer(&ctx, in, 64);
      uint64_t loc=store(hash,(char*)in,SEED);
      if(loc!=-1){hash[loc]=1;}
    }
  }

	return 0;
}